package Ch01;

public class C02Escape {

	public static void main(String[] args) {
		// format
		// ESCAPE문자 - 개행문자(\n, \r)
		System.out.printf("ESCAPE문자 확인\n");
		System.out.printf("ESCAPE문자 확인\r");
		System.out.printf("개행문자 확인 \\n \\r \n");
		// 자동개행메서드 - println
		System.out.println("자동개행메서드");
		// ESCAPE문자  - (\t == tap)
		System.out.println("apple\tOrange");
		// 따옴표 출력 - \"
		System.out.println("따옴표  \"hello\" 출력");
		
	}

}
