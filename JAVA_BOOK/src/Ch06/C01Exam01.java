package Ch06;

import java.util.Scanner;

public class C01Exam01 {

	public static void main(String[] args) {
		// 두 수를 입력받아, 3의 배수이면 출력
		Scanner scan = new Scanner(System.in);
		int iNum1 = scan.nextInt();
		int iNum2 = scan.nextInt();
		
		if(iNum1%3==0) {
			System.out.println();
		}
		if(iNum2%3==0) {
			System.out.println();
		}
				
	}

}
